void paletee_print_color_select
(
	uint32 x, 
	uint32 y, 
	paletee_256_type_t color
)
{
	uint8 * pChFont = NULL;
	uint32 line = 0;
	uint32 pixel = 0;
	uint8 data = 0;
	uint8 bit = 7;

	/* 将color点阵值写入对应调色板位置 */
	for (line = y; line < (y + PALETEE_COLOR_WIDTH); line++)
	{
		for (pixel = x; pixel < (x + PALETEE_COLOR_WIDTH); pixel++)
		{
			frameBuffer_set_point(pixel, line, color);
		}
	}
}

